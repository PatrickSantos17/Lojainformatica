//DAO para atualizar objs da tabela
    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed

        // 1º Resgatar dados da linha da tabela e passar para o objeto
        int linhaSelecionada = jTable1.getSelectedRow();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        int idSelecionado = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
        String MarcaSelecionada = modelo.getValueAt(linhaSelecionada, 1).toString();
        String hdSelecionado = modelo.getValueAt(linhaSelecionada, 2).toString();
        String ProcessadorSelecionado = modelo.getValueAt(linhaSelecionada, 3).toString();

        Computador objAlterar = new Computador(idSelecionado, hdSelecionado, ProcessadorSelecionado);

        // TODO: Passar objeto para a tela de alteração
        ViewComputador alterarobj = new ViewComputador(objAlterar);
        alterarobj.setVisible(true);
        
        recarregarLista();
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO: Resgatar o ID da linha selecionada e enviar para a DAO
        int linhaSelecionada = jTable1.getSelectedRow();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int idExcluir = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());

        // Mandar para a DAO:
        boolean retorno = ComputadorDAO.excluir(idExcluir);
        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Falha!");
        }

        recarregarLista();


    }//GEN-LAST:event_btnExcluirActionPerformed

