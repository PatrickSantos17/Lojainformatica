    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
      if(txtBuscar.getText().trim().equals("")){
               recarregarLista();
      } else{        
        // TODO: Chamar a DAO
        
        String buscar = txtBuscar.getText();
        ArrayList<Computador> lista = ComputadorDAO.buscarPorProcessador(buscar);

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        for (Computador it : lista) {
            modelo.addRow(new String[]{
                String.valueOf(it.getIdComputador()),
                String.valueOf(it.getMarca()),
                String.valueOf(it.getHd()),
                String.valueOf(it.getProcessador())
            });
        }
      }
    }//GEN-LAST:event_btnBuscarActionPerformed
    private  void recarregarLista() {
        ArrayList<Computador> lista = ComputadorDAO.listar();

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        for (Computador it : lista) {
            modelo.addRow(new String[]{
                String.valueOf(it.getIdComputador()),
                String.valueOf(it.getMarca()),
                String.valueOf(it.getHd()),
                String.valueOf(it.getProcessador())
            });
        }
    }

